    public String getProcessName(){
        String name = nome.getText();
        this.tabelaTarefa.setValueAt(CurrentIndex, CurrentIndex, 0);
        this.tabelaTarefa.setValueAt(name, CurrentIndex, 1);
        nome.setText("");
        return name;
    }
    
    public int getWeight() {
        int  weight = Integer.parseInt(carga.getText());
        carga.setText("");
        return weight;
    }
    
    public int getArrive() {
        int arrive = Integer.parseInt(this.tempoDeChegada.getText());
        tempoDeChegada.setText("");
        return arrive;
    }
    
    public int getDeadline() {
        int deadline = Integer.parseInt(this.deadline.getText());
        this.deadline.setText("");
        return deadline;
    }
    
    public int getID() {
        int id = CurrentIndex;
        CurrentIndex++;
        return id;
    }
        
