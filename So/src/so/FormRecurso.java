    public String getResourceName() {
        String name = nome.getText();
        this.tabelaRecurso.setValueAt(CurrentIndex, CurrentIndex, 0);
        this.tabelaRecurso.setValueAt(name, CurrentIndex, 1);
        nome.setText("");
        return name;
    }
    
